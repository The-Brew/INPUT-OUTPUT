// wap to accept a character between a to j and print it into next four character//
#include<bits/stdc++.h>
using namespace std;
 int main()
 {
     char ch;
     cout<<"\n enter a char b/w A and J::";
     cin>>ch;
     cout<<"\n"<<(char)(ch+1);
     cout<<"\n"<<(char)(ch+2);
     cout<<"\n"<<(char)(ch+3);
     cout<<"\n"<<(char)(ch+4);
    
     return 0;
 }